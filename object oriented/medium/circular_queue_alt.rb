class CircularQueue
  def initialize(length)
    @items = []
    @length = length
  end
  def enqueue(value)
    @items.push(value)
    @items.shift if @items.size > @length
  end
  def dequeue
    @items.shift
  end
end

queue = CircularQueue.new(3)
puts queue.dequeue == nil

queue.enqueue(1)
queue.enqueue(2)
puts queue.dequeue == 1

queue.enqueue(3)
queue.enqueue(4)
puts queue.dequeue == 2

queue.enqueue(5)
